package modularizacion.ejercicio1;

class suma {
    int num1,num2, suma;

    suma(int n1, int n2){
        num1 = n1;
        num2 = n2;
        suma = n1+n2;
    }
}
class resta {
    int num1,num2, resta;

    resta(int n1, int n2){
        num1 = n1;
        num2 = n2;
        resta = n1-n2;
    }
}

class multiplicacion {
    int num1,num2, multiplicacion;

    multiplicacion(int n1, int n2){
        num1 = n1;
        num2 = n2;
        multiplicacion = n1*n2;
    }
}

class division {
    int num1,num2, division;

    division(int n1, int n2){
        num1 = n1;
        num2 = n2;
        division = n1/n2;
    }
}